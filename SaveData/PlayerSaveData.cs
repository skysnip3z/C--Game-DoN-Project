public class PlayerSaveData
{
    public int maxHealth;
    public int toughness;
    public int attackPower;
    public int narikPoints;

    public PlayerSaveData(int maxh, int tough, int atkp, int narikp) 
    {
        maxHealth = maxh;
        toughness = tough;
        attackPower = atkp;
        narikPoints = narikp;
    }
}
